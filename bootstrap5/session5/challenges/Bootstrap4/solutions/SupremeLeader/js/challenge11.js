function display_reviews() {


    let hotel_button_div_str = "";
    let hotel_info_div_str = "";

    counter = 1;
    for(var hotel of Object.entries(hotels)) {
        console.log(hotel);

        let name = hotel[0];
        let address = hotel[1][0];
        let phone = hotel[1][1];
        let ranking = hotel[1][2];
        let image_url = hotel[1][3];
        let reviews = hotel[1][4]; // object


        hotel_button_div_str += `
            <button style="margin-bottom: 5px; width: 200px;" class="btn btn-primary" type="button" data-toggle="collapse" data-target="#hotel${counter}">
                ${name}
            </button>
        `;
        

        let show = "";
        if( counter == 1 ) {
            show = " show";
        }

        hotel_info_div_str += `
            <!-- Hotel ${counter} -->
            <div class="collapse${show}" id="hotel${counter}">
                <div class="card card-body">
                    <div class="container-fluid">
                        <h2 style="font-weight: bold">${name}</h2>
                        <ul class="list-unstyled">
                            <li>
                                <img src="images/${image_url}">
                            </li>
                            <li>
                                <i class="fas fa-map-marked-alt"></i>
                                ${address}
                            </li>
                            <li>
                                <i class="fas fa-phone"></i>
                                ${phone}
                            </li>
                            <li>
                                <i class="fas fa-star"></i>
                                ${ranking}
                            </li>
                        </ul>
                    </div>

                    <hr class="my-3">
        `;

        // Append reviews
        let hotel_reviews_str = `
                    <div class="container-fluid">
        `;

        //console.log(reviews);
        for(var review of Object.entries(reviews)) {

            let reviewer_name = review[0];
            let review_comment = review[1];

            hotel_reviews_str += `
                        <blockquote class="blockquote">
                            <p class="mb-0">${review_comment}</p>
                            <footer class="blockquote-footer">
                                <cite>${reviewer_name}</cite>
                            </footer>
                        </blockquote>
            `;
        }

        // Append hotel_reviews_str to hotel_info_div_str
        hotel_info_div_str += hotel_reviews_str;

        // End Quotes section
        hotel_info_div_str += `
                    </div> <!-- End of Quotes -->
        `;

        // End Card Body & Collapse divs
        hotel_info_div_str += `
                </div> <!-- End of Card Body -->
            </div> <!-- End of Collapse -->
            `;

        counter++;
    }

    

    // Populate 'hotel_buttons_div' section of HTML
    var hotel_button_div = document.getElementById('hotel_button_div');
    //console.log(hotel_button_div);
    hotel_button_div.innerHTML = hotel_button_div_str;


    // Populate 'hotel_info_div' section of HTML
    var hotel_info_div = document.getElementById('hotel_info_div');
    console.log(hotel_info_div);
    hotel_info_div.innerHTML = hotel_info_div_str;

}